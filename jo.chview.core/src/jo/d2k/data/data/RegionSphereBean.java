package jo.d2k.data.data;


public class RegionSphereBean extends RegionBean 
{
    private double  mX;
    private double  mY;
    private double  mZ;
    private double  mR;
    
    public double getX()
    {
        return mX;
    }
    public void setX(double x)
    {
        mX = x;
    }
    public double getY()
    {
        return mY;
    }
    public void setY(double y)
    {
        mY = y;
    }
    public double getZ()
    {
        return mZ;
    }
    public void setZ(double z)
    {
        mZ = z;
    }
    public double getR()
    {
        return mR;
    }
    public void setR(double r)
    {
        mR = r;
    }
}
